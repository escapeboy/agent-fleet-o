  {{-- Integration with Workflows --}}
  <h2 class="mt-12 text-2xl font-semibold tracking-tight text-gray-900" id="workflow-integration">Integration with Workflows</h2>
  <p class="mt-4 text-gray-600">Human Task nodes are first-class citizens of the workflow graph. See the <a href="{{ route('docs.show', 'workflows') }}" class="text-primary-600 hover:underline">Workflows documentation</a> for full details on building visual DAGs.</p>
  <p class="mt-4 text-gray-600">Key points when using Human Tasks inside a workflow:</p>
  <ul class="mt-4 list-disc list-inside space-y-2 text-gray-600">
    <li>A <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">human_task</code> node <strong>pauses workflow execution</strong>. No nodes downstream of it will run until the task is completed or expires.</li>
    <li>The <code>form_schema</code> is defined on the workflow node configuration and may reference upstream node outputs using template variables.</li>
    <li>Once the task is completed, the submitted <code>form_data</code> object becomes the output of the node and is available to all downstream nodes via the standard <code>{{ '{{node_id.field_name}}' }}</code> template syntax.</li>
    <li>Add an error edge from every <code>human_task</code> node to handle the case where the task expires without a response.</li>
  </ul>
  <x-docs.code language="json">
{
  "id": "review-copy",
  "type": "human_task",
  "label": "Review campaign copy",
  "config": {
    "assigned_role": "admin",
    "sla_hours": 24,
    "form_schema": {
      "type": "object",
      "required": ["approved"],
      "properties": {
        "approved": { "type": "boolean", "title": "Approve this copy?" },
        "feedback": { "type": "string", "title": "Feedback for the writer" }
      }
    }
  }
}
  </x-docs.code>
  <x-docs.callout type="tip" title="Chaining Human Tasks">
    You can place multiple <code>human_task</code> nodes in sequence to implement multi-stage review processes — for example, a writer review followed by a legal sign-off.
  </x-docs.callout>
</x-layouts.docs>

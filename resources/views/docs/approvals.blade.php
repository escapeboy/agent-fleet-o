    </table>
  </div>
  <p class="mt-4 text-gray-600">The <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">approvals:expire-stale</code> scheduled command runs every hour and moves overdue <code>pending</code> requests to <code>expired</code>.</p>

  {{-- Human Tasks --}}
  <h2 class="mt-12 text-2xl font-semibold tracking-tight text-gray-900" id="human-tasks">Human Tasks</h2>
  <p class="mt-4 text-gray-600">A Human Task is an <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">ApprovalRequest</code> that carries a <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">form_schema</code> JSON Schema. The schema defines the fields that must be completed before the workflow can continue.</p>
  <p class="mt-4 text-gray-600">When a workflow reaches a <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">human_task</code> node:</p>
  <ol class="mt-4 list-decimal list-inside space-y-2 text-gray-600">
    <li>The platform creates an <code>ApprovalRequest</code> linked to the node, with the node's <code>form_schema</code> and the assigned user or role.</li>
    <li>The workflow execution pauses — no downstream nodes run.</li>
    <li>The assigned user sees the task in the Approval Inbox and opens the <code>HumanTaskForm</code> Livewire slide-over.</li>
    <li>On submission the form data is validated against <code>form_schema</code> and stored on the request.</li>
    <li>The workflow resumes, and the next node receives the submitted data as its input payload.</li>
  </ol>

  <h3 class="mt-8 text-lg font-semibold text-gray-900">Example form_schema</h3>
  <x-docs.code language="json">
{
  "type": "object",
  "required": ["decision", "notes"],
  "properties": {
    "decision": {
      "type": "string",
      "title": "Decision",
      "enum": ["approve", "revise", "discard"],
      "description": "Select the outcome for this campaign draft."
    },
    "notes": {
      "type": "string",
      "title": "Notes",
      "description": "Provide feedback for the AI agent.",
      "minLength": 10
    },
    "budget_override": {
      "type": "number",
      "title": "Budget Override (€)",
      "description": "Optional: override the proposed budget ceiling."
    }
  }
}
  </x-docs.code>
  <x-docs.callout type="tip" title="Rendering">
    The <code>HumanTaskForm</code> component renders each property as a form field using its JSON Schema type and title. String enums become dropdowns. Boolean fields become checkboxes.
  </x-docs.callout>

  {{-- SLA Enforcement --}}
  <h2 class="mt-12 text-2xl font-semibold tracking-tight text-gray-900" id="sla">SLA Enforcement</h2>
  <p class="mt-4 text-gray-600">Human Tasks can carry an SLA deadline. If the task is not completed by that deadline, the platform escalates or expires it automatically.</p>
  <p class="mt-4 text-gray-600">The <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">human-tasks:check-sla</code> scheduled command runs <strong>every 5 minutes</strong> and performs two checks:</p>
  <ul class="mt-4 list-disc list-inside space-y-2 text-gray-600">
    <li><strong>Escalation</strong> — if the task is approaching its deadline (configurable warning window), team admins and the assigned user receive a notification.</li>
    <li><strong>Expiry</strong> — if the deadline has passed, the task is marked <code>expired</code> and the blocked workflow node is failed, allowing the workflow to continue along its error path.</li>
  </ul>
  <x-docs.callout type="warning" title="Expired tasks fail the workflow node">
    When a Human Task expires the associated workflow node transitions to a failed state. Make sure your workflow graph includes an error edge from <code>human_task</code> nodes to a recovery path, or the workflow execution will terminate.
  </x-docs.callout>

  {{-- Webhook Notifications --}}
  <h2 class="mt-12 text-2xl font-semibold tracking-tight text-gray-900" id="webhooks">Webhook Notifications</h2>
  <p class="mt-4 text-gray-600">You can configure a webhook URL to receive real-time events whenever an approval is created, approved, rejected, or expires. This is useful for integrating approval state into external tools such as Slack, Jira, or a custom dashboard.</p>
  <p class="mt-4 text-gray-600">Events are sent as <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">POST</code> requests with a JSON body:</p>
  <x-docs.code language="json">
{
  "event": "approval.approved",
  "approval_request_id": "019501de-...",
  "status": "approved",
  "decided_by": "user@example.com",
  "decided_at": "2026-03-21T14:05:00Z",
  "context": { ... }
}
  </x-docs.code>
  <p class="mt-4 text-gray-600">Configure the webhook endpoint using the <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">approval_webhook_config</code> MCP tool or via the Team Settings page.</p>


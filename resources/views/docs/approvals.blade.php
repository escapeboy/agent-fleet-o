<x-layouts.docs title="Approvals & Human Tasks" description="Human-in-the-loop review and structured human input for AI workflows." page="approvals">
  <h1 class="text-3xl font-bold tracking-tight text-gray-900">Approvals &amp; Human Tasks</h1>
  <p class="mt-4 text-lg text-gray-600">Put humans in the loop at any point in an AI workflow. Approvals let a person review and authorise an AI decision before it takes effect. Human Tasks present structured forms that collect input from a user and feed the response into downstream workflow nodes.</p>

  {{-- Overview --}}
  <h2 class="mt-12 text-2xl font-semibold tracking-tight text-gray-900" id="overview">Overview</h2>
  <p class="mt-4 text-gray-600">Both Approvals and Human Tasks are backed by the same <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">ApprovalRequest</code> model. The distinction is in how they are created and completed:</p>
  <ul class="mt-4 list-disc list-inside space-y-2 text-gray-600">
    <li><strong>Approvals</strong> — generated automatically by the platform when an action requires human sign-off. A reviewer approves or rejects the request.</li>
    <li><strong>Human Tasks</strong> — generated by a workflow <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">human_task</code> node. A form schema defines the fields the user must fill in. The submitted data is passed as input to downstream nodes.</li>
  </ul>
  <x-docs.callout type="info" title="Same model, different intent">
    An <code>ApprovalRequest</code> becomes a Human Task when it is linked to a workflow node and carries a <code>form_schema</code>. Standard approvals have no form schema — they only require an approve or reject decision.
  </x-docs.callout>

  {{-- When Approvals are Required --}}
  <h2 class="mt-12 text-2xl font-semibold tracking-tight text-gray-900" id="when-required">When Approvals are Required</h2>
  <p class="mt-4 text-gray-600">Approvals are triggered in the following scenarios:</p>
  <ul class="mt-4 list-disc list-inside space-y-2 text-gray-600">
    <li><strong>Experiment <code>AwaitingApproval</code> state</strong> — when an experiment transitions to <code>AwaitingApproval</code>, execution is halted until a team member approves the plan. On approval the experiment advances to <code>Approved</code> and then to <code>Executing</code>. On rejection it returns to <code>Planning</code>.</li>
    <li><strong>Outbound delivery</strong> — certain outbound connectors can be configured to require approval before messages are sent, preventing automated sending without human review.</li>
    <li><strong>Workflow <code>human_task</code> nodes</strong> — a node of this type pauses the workflow graph and creates a Human Task for a designated user or role.</li>
    <li><strong>Budget threshold alerts</strong> — when spending approaches plan limits the platform may pause an experiment and request manual review before continuing.</li>
  </ul>

  {{-- Approval Inbox --}}
  <h2 class="mt-12 text-2xl font-semibold tracking-tight text-gray-900" id="inbox">Approval Inbox</h2>
  <p class="mt-4 text-gray-600">The Approval Inbox is available at <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">/approvals</code>. It lists all pending requests for the current team, ordered by creation time.</p>
  <ul class="mt-4 list-disc list-inside space-y-2 text-gray-600">
    <li>Each request shows the context payload — what the AI was about to do and why approval is needed.</li>
    <li>Inline <strong>Approve</strong> and <strong>Reject</strong> buttons act immediately without a page reload.</li>
    <li>For Human Tasks, an <strong>Open Form</strong> button opens the <code>HumanTaskForm</code> Livewire component in a slide-over panel.</li>
    <li>A status filter lets you review historical decisions (approved, rejected, expired).</li>
  </ul>
  <x-docs.callout type="tip" title="Notification Bell">
    Pending approvals also appear in the notification bell in the top navigation bar. Clicking a notification takes you directly to the relevant request.
  </x-docs.callout>


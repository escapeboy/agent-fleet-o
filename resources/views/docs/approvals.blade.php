  <h2 class="mt-12 text-2xl font-semibold tracking-tight text-gray-900" id="statuses">Approval Statuses</h2>
  <p class="mt-4 text-gray-600">Every <code class="text-sm font-mono bg-gray-100 px-1.5 py-0.5 rounded">ApprovalRequest</code> moves through the following statuses:</p>
  <div class="mt-6 overflow-hidden rounded-xl border border-gray-200">

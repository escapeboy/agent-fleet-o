      </thead>
      <tbody class="divide-y divide-gray-200 bg-white">
        <tr>
          <td class="px-4 py-3 font-mono text-gray-800">pending</td>
          <td class="px-4 py-3 text-gray-600">Awaiting a human decision.</td>
          <td class="px-4 py-3 text-gray-600">Approve, reject, or wait for expiry.</td>
        </tr>
        <tr>
          <td class="px-4 py-3 font-mono text-gray-800">approved</td>
          <td class="px-4 py-3 text-gray-600">A team member approved the request. The blocked workflow or experiment resumes automatically.</td>
          <td class="px-4 py-3 text-gray-600">Terminal — no further action.</td>
        </tr>
        <tr>
          <td class="px-4 py-3 font-mono text-gray-800">rejected</td>
          <td class="px-4 py-3 text-gray-600">A team member rejected the request. Dependent experiments return to their previous state for revision.</td>

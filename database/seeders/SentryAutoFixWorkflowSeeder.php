# Sensitive paths — special handling
When your change touches any of these, **pause and double-check** for multi-tenancy / auth / billing / migration risk; if unsure, abort with `experiment_complete_building summary="aborted: sensitive-path change required human review"`:
- `app/Domain/Shared/**` (multi-tenancy core)
- `app/Infrastructure/AI/Services/ProviderResolver.php` (auth/billing)
- `database/migrations/**` (schema risk)
